import java.io.Serializable;

public class Animal implements Serializable {
    private static final long serialVersionUID = 8779257981431896541L;

    private String id;
    private String name;
    private String type;
    private int height;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public String toString() {
        return "id=" + getId() + 
                " name=" + getName() + 
                " type=" + getType() + 
                " height=" + getHeight();
    }
}